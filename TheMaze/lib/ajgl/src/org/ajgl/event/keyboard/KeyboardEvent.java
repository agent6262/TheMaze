package org.ajgl.event.keyboard;

import org.ajgl.event.Event;

/**
 * This class is designed to be extended by higher level
 * keyboard events.
 * @author Tyler Bucher
 */
public abstract class KeyboardEvent extends Event {
    
    private boolean canceled;   // States weather the event has been canceled

    @Override
    public void setCanceled(boolean canceled) {
        this.canceled = canceled;
    }

    @Override
    public boolean isCanceled() {
        return canceled;
    }
    
    @Override
    public String getName() {
        return "KeyboardEvent";
    }
}
